public class Row {

    private int id;
    private int a;
    private int b;

    public Row(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getId() {
        return id;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }
}
